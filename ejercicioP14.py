# leer número
numero = float(input("ingrese un numero entero o decimal: ")) #float para que se puedan ingresar números decimales
# calcular cuadrado y cubo de dicho número
cuadrado = numero ** 2
cubo = numero ** 3
# resultados
print(f"El cuadrado de {numero} es: {cuadrado}")
print(f"El cubo de {numero} es: {cubo}")